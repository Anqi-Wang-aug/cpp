/* (As an exercise of for loop)
 * This program: 
 * 1. adds numbers from 1 to 10
 */
 
#include <iostream>
int main()
{
	int sum = 0;
	for(int val = 1; val <=10; val++)
	{
		sum+=val;
	}
	std::cout << "The sum from 1 to 10 is " << sum << std::endl;
	return 0;
}
